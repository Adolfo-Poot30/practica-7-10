    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        int tamaño = 0;
        try {
            tamaño = Integer.parseInt(jTextFieldFilas.getText());
        } catch (Exception Exp) {
            JOptionPane.showMessageDialog(null,"Ingresar solo números enteros, Error: "+Exp.getMessage());
            return;
        }
        
        int matriz[][];
        if (tamaño > 0) {            
            matriz = new int[tamaño][tamaño];
            matriz = new operacionesDeMatrices().llenarMatrizCuadrada(matriz);
            if(matriz != null){
            new VerificarSiEsCuadradoMagico().verificarCuadradoMagico(matriz, jTextArea1);
            }
        } else {
            JOptionPane.showMessageDialog(null,"Ingresa un número entero positivo");
        }
        
    }//GEN-LAST:event_jButtonAceptarActionPerformed


